def org_short(pr):
    return pr.split("、")[0].split("（")[0].split("(")[0]

def main():
    os.makedirs(DATA, exist_ok=True)
    projects=[]
    for p in M:
        direct = "url" in p and not p.get("search")
        link = p["url"] if direct else ("https://www.google.com/search?q=" +
                urllib.parse.quote(p["name"]+" "+p["pr"]+" 公式"))
        tier=tier_of(p["budN"])
        projects.append({
            "cat":p["cat"],"category_ja":CATS[p["cat"]]["ja"],
            "name":p["name"],"status":p["status"],
            "bud":p["bud"],"budget":p["bud"],
            "budN":p["budN"],"budget_tn_yen":p["budN"],
            "tl":p["tl"],"timeline":p["tl"],
            "pr":p["pr"],"proponent":p["pr"],"desc":p["desc"],
            "tags":p["tags"],"at":[p["at"][0],p["at"][1]],
            "lat":p["at"][0],"lng":p["at"][1],
            "route":p.get("route"),
            "region":p["region"],"feas":p["feas"],"feas_ja":FEAS[p["feas"]]["ja"],
            "tier":tier,
            "link":link,"link_direct":direct,
            "link_label":org_short(p["pr"])+("公式サイト" if direct else "公式情報を検索"),
        })

    payload={"updated":UPDATED,"count":len(projects),
             "total_tn_yen":round(sum(p["budget_tn_yen"] for p in projects),1),
             "categories":CATS,"feas":FEAS,"regions":REGIONS,"tiers":TIERS,
             "routes":ROUTES,"projects":projects}

    with open(os.path.join(DATA,"mega_projects.json"),"w",encoding="utf-8") as f:
        json.dump(payload,f,ensure_ascii=False,indent=2)

    cols=["category_ja","name","region","feas_ja","status","budget","budget_tn_yen","timeline","proponent","tags","lat","lng","link"]
    buf=io.StringIO(); w=csv.writer(buf); w.writerow(cols)
    for p in projects:
        w.writerow([p["category_ja"],p["name"],p["region"],p["feas_ja"],p["status"],p["budget"],
                    p["budget_tn_yen"],p["timeline"],p["proponent"],"|".join(p["tags"]),p["lat"],p["lng"],p["link"]])
    with open(os.path.join(DATA,"mega_projects.csv"),"w",encoding="utf-8-sig") as f:
        f.write(buf.getvalue())

    src_geo=os.path.join(os.path.dirname(__file__),"japan.geojson")
    geo=open(src_geo,encoding="utf-8").read().strip()
    open(os.path.join(DATA,"japan.geojson"),"w",encoding="utf-8").write(geo)

    data_json=json.dumps(payload,ensure_ascii=False,separators=(",",":"))
    for tpl_name,out_name in [("index.template.html","index.html"),("search.template.html","search.html")]:
        html=open(os.path.join(TPL,tpl_name),encoding="utf-8").read()
        html=(html.replace("__UPDATED__",UPDATED).replace("__DATA_JSON__",data_json).replace("__GEO_JSON__",geo))
        open(os.path.join(ROOT,out_name),"w",encoding="utf-8").write(html)

    print(f"[build] {len(projects)} projects · total ≈{payload['total_tn_yen']}兆円 · updated {UPDATED}")
    from collections import Counter
    rc=Counter(p['region'] for p in projects)
    print("[build] region counts:", dict((r,rc[r]) for r in REGIONS))

if __name__=="__main__":
    main()
